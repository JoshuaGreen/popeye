#if !defined(STIPULATION_SERIES_PLAY_PLAY_H)
#define STIPULATION_SERIES_PLAY_PLAY_H

/* Interface for dynamically dispatching series play functions to slices
 * depending on the slice type
 */

#include "pyslice.h"

/* Solve in a number of half-moves
 * @param si identifies slice
 * @param n exact number of half moves until end state has to be reached
 * @return length of solution found, i.e.:
 *         n+2 the move leading to the current position has turned out
 *             to be illegal
 *         n+1 no solution found
 *         n   solution found
 */
stip_length_type series(slice_index si, stip_length_type n);

/* Determine whether there is a solution in n half moves.
 * @param si slice index of slice being solved
 * @param n exact number of half moves until end state has to be reached
 * @return length of solution found, i.e.:
 *         n+2 the move leading to the current position has turned out
 *             to be illegal
 *         n+1 no solution found
 *         n   solution found
 */
stip_length_type has_series(slice_index si, stip_length_type n);

#endif
